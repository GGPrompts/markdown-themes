/**
 * Terminal color palettes for each app theme.
 *
 * Each palette defines foreground, cursor, selection, and full 16-color ANSI
 * palette. Background is always transparent — the CSS gradient in TerminalPanel
 * shows through.
 *
 * ANSI color usage in terminal output (ls, git, Claude CLI):
 * - Red: Errors, critical issues
 * - Green: Success, directories (ls), additions (git diff)
 * - Yellow: Warnings, important notes
 * - Blue: File paths, links, references
 * - Magenta: Tool names, function calls
 * - Cyan: Headers, section dividers
 * - brightBlack: Metadata, timestamps, comments
 */

import type { ThemeId } from '../themes';

export interface TerminalPalette {
  foreground: string;
  cursor: string;
  cursorAccent: string;
  selectionBackground: string;
  black: string;
  red: string;
  green: string;
  yellow: string;
  blue: string;
  magenta: string;
  cyan: string;
  white: string;
  brightBlack: string;
  brightRed: string;
  brightGreen: string;
  brightYellow: string;
  brightBlue: string;
  brightMagenta: string;
  brightCyan: string;
  brightWhite: string;
}

/**
 * Build an xterm.js ITheme from a palette. Always sets background to transparent.
 */
export function buildXtermTheme(palette: TerminalPalette) {
  return {
    background: 'transparent',
    foreground: palette.foreground,
    cursor: palette.cursor,
    cursorAccent: palette.cursorAccent,
    selectionBackground: palette.selectionBackground,
    selectionForeground: undefined,
    black: palette.black,
    red: palette.red,
    green: palette.green,
    yellow: palette.yellow,
    blue: palette.blue,
    magenta: palette.magenta,
    cyan: palette.cyan,
    white: palette.white,
    brightBlack: palette.brightBlack,
    brightRed: palette.brightRed,
    brightGreen: palette.brightGreen,
    brightYellow: palette.brightYellow,
    brightBlue: palette.brightBlue,
    brightMagenta: palette.brightMagenta,
    brightCyan: palette.brightCyan,
    brightWhite: palette.brightWhite,
  };
}

